def format_category(category_jp):
    """格式化分類顯示：トップス / 上衣 / Tops"""
    if category_jp in CATEGORY_TRANSLATIONS:
        trans = CATEGORY_TRANSLATIONS[category_jp]
        return f"{category_jp} / {trans['zh']} / {trans['en']}"
    return category_jp

